#Write a python program that is able to calculate the BMI of a person whose weight is 78kgs and height is 1.75 meters. BMI = (weight) / (height squared)
Weight = 78 
Height = 1.75 
BMI = (Weight) / (Height**2)
print("The BMI is: ", BMI)


